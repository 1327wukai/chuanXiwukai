package com.wukai.forCycle;

public class whiledemo {
    public static void main(String[] args) {
        int i = 0;
        while (i < 3) {
            System.out.println(i);
            i++;
        }


        int g = 0;
        do {
            System.out.println(g);
            g++;
        }
        while (g < 3);
    }


}